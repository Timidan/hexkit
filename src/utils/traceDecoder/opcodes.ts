// Helper to get static gas cost for an opcode
export function getStaticGasCost(opcodeName: string): number {
  const cost = STATIC_GAS_COSTS[opcodeName];
  // Use nullish coalescing to handle 0 correctly (0 is a valid gas cost for STOP, RETURN, REVERT)
  return cost ?? 3; // Default to 3 for unknown opcodes
}
